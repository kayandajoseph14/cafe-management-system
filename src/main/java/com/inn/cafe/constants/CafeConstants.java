package com.inn.cafe.constants;

/**
 * This class holds the constant values used throughout the cafe application.
 * These constants can be referenced in various parts of the application
 * to maintain consistency in messages and reduce duplication.
 */
public class CafeConstants {

    // Message returned when an unexpected error occurs.
    public static final String SOMETHING_WENT_WRONG = "Something Went Wrong.";

    // Message returned when the provided data is invalid.
    public static final String INVALID_DATA = "Invalid Data.";

    // Message returned when a user attempts to register with an email that already exists.
    public static final String EMAIL_ALREADY_EXIST = "Email Already Exist.";

    // Message returned upon successful user registration.
    public static final String SUCCESSFUL_REGISTERED = "Successful Registered.";

    public static final String UNAUTHORIZED_ACCESS = "Unauthorized Access";
}
